    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        dsrt.setRealisasiPanen(Integer.valueOf(txtRealisasiPanen.getText().trim()));
        dsrt.setBerhasilPanen(checkBerhasil.isSelected() == true ? 1 : 0);
        dsrt.setAlasanTidakBerhasilPanen(txtAlasanTidakBerhasilPanen.getText());
        dsrt.setUbinanBersama(checkbersama.isSelected() == true ? 1 : 0);
        dsrt.setHasilUbinan(Double.valueOf(txtHasilUbinan.getText()));
        dsrt.setUsername(username);
        try {
            long result = DSRTService.updateDSRT(dsrt);
            if (result == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan.", "Info", JOptionPane.INFORMATION_MESSAGE);
                loadAwal();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormEntri.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.resetForm();
        this.mainFrame.getContentScrollPane().setViewportView(new FormDaftarDSRT(this.mainFrame, username));
    }//GEN-LAST:event_buttonSimpanActionPerformed

