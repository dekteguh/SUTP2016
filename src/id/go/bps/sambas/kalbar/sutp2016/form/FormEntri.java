    private void loadAwal() {
        txtRealisasiPanen.requestFocus();
        txtTahun.setText(dsrt.getTahun().toString());
        txtSubround.setText(dsrt.getSubround().toString());
        String idProv = dsrt.getIdProv();
        String idKab = dsrt.getIdKab();
        Properties prop = null;
        try {
            prop = getWilayah(idProv, idKab);
        } catch (Exception ex) {
            Logger.getLogger(FormEntri.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtProvinsi.setText(prop.getProperty("id_prov")+" - "+prop.getProperty("nama_prov"));
        txtKabupaten.setText(prop.getProperty("id_kab")+" - "+prop.getProperty("nama_kab"));
        txtNKS.setText(dsrt.getNks());
        txtNURT.setText(dsrt.getNurtSampel().toString());
        txtNamaKRT.setText(dsrt.getNamaKrt());
        txtJenissampel.setText(dsrt.getJenisSampel());
        txtJenisTanaman.setText(dsrt.getJenisTanaman());
        txtPerkiraanPanen.setText(dsrt.getPerkiraanPanen().toString());
    }

